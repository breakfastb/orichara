$(document).ready(function() {
	// get current pathname
	var pathname = (window.location.pathname).toString().substr(10);
	var current_url = pathname.substr(0, pathname.length - 5);
	console.log(current_url);

	//search works array for pathname
	function matchTitle(title) {
		return title.label === current_url;
	}

	//match info from title
	var matched_title = charas.find(matchTitle);

	//heading info
	$('#characters').find('h1').text(matched_title.title);
	$('#characters').find('.desc').text(matched_title.blurb);
	
	//add buttons to description
	$.each(matched_title.link, function(k, v) {
		var btn = $('#btnTemplate').clone().removeAttr('id');
		btn.attr('href', v.url).text(v.label);

		if (v.display != 0) {
			btn.appendTo($('.detail'));
		}
		else {
			$('.detail').find(btn).remove();
		}
	});

	//character info
	$.each(matched_title.characters, function(item, descriptor) {
		var prof = $('#template').clone().removeAttr('id');
		prof.find('.name').text(descriptor.name);
		prof.find('.blurb').text(descriptor.desc);

		$.each(descriptor.info, function(key, valueObj) {
			var label = '<strong>' + key + ' // </strong> ';
			var liItem = $('#infoTemplate').clone().removeAttr('id');
			liItem.text(valueObj).prepend(label);
			liItem.appendTo(prof.find('.infoList'));
		});

		$.each(descriptor.gallery, function(gallery, url) {
			var img = $('#galleryTemplate').clone().removeAttr('id');
			var profilePic = prof.find('.profile');
			img.attr('href', url);
			img.find('img').attr('src', url);
			img.appendTo(prof.find('.gallery'));
			profilePic.attr('src', './img/profile/' + descriptor.name.toLowerCase() + '.png');
		});

		//append
		prof.appendTo($('#characters'));
	});
	
	//hr
	$("<hr>").insertBefore($('.chara').not($('#template')));
	
	//nav menu
	if($(window).width() < 996) {
		$('.menu-toggle').on('click',function() {
			$('#side-menu').stop().slideToggle(200,'linear');
		});
	}
});
